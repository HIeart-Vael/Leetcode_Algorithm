/***********************************************************************************
*
* 题目名称：509. 斐波那契数
* 题目描述：斐波那契数列用 F(n) 表示，给你 n ，请计算 F(n) 。
*
* 创建时间：2021-08-07 12:22
*
***********************************************************************************/

class Solution {
public:
    int fib(int n) {
        int *a = new int[n + 2];
        a[0] = 0;
        a[1] = 1;
        for (int i = 2; i < n + 1; i++) {
            a[i] = a[i - 1] + a[i - 2];
        }
        return a[n];
    }
};